CEnemySpawner* CEnemySpawner::Create(const D3DXVECTOR3& Activepos)
{
	// インスタンス生成
	CEnemySpawner* pSpawn = new CEnemySpawner;
	if (pSpawn == nullptr) return nullptr;

	// 引数設定
	pSpawn->SetPos(Activepos);

	// 初期化失敗時
	if (pSpawn->Init()) return nullptr;

	return pSpawn;
}
