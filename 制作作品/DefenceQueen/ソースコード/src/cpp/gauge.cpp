//=========================================================
// コンストラクタ
//=========================================================
CGauge::CGauge(int nPriority) : CObject2D(nPriority)
{
	// 無し
}
//=========================================================
// デストラクタ
//=========================================================
CGauge::~CGauge()
{
	
}
//=========================================================
// 生成処理
//=========================================================
CGauge* CGauge::Create(const D3DXVECTOR3& pos, float fWidth, float fHeight)
{
	// インスタンス生成
	CGauge* pGage = new CGauge;
	if (pGage == nullptr) return nullptr;

	// 初期化失敗時
	if (FAILED(pGage->Init())) return nullptr;

	// オブジェクト設定
	pGage->SetPos(pos);
	pGage->SetSize(fWidth, fHeight);

	// ポインタを返す
	return pGage;
}
//=========================================================
// 初期化処理
//=========================================================
HRESULT CGauge::Init(void)
{
	// 親クラスの初期化
	CObject2D::Init();

	// 初期化結果を返す
	return S_OK;
}
//=========================================================
// 終了処理
